package com.rene.messenger.model;

public class Link {
	
	private String url;
	private String rel;
	
	public String getUrl() {
		return this.url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getRel() {
		return this.rel;
	}
	public void setRel(String rel) {
		this.rel = rel;
	}
	
	

}
